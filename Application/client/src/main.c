/**********************************************************************************
 * HEADERS
**********************************************************************************/

#include <stdio.h>

/**********************************************************************************
 * INTERFACES
**********************************************************************************/

#include "client.h"

/**********************************************************************************
 * MAIN
**********************************************************************************/

int main(int argc, char *argv[])
{
    int ret_val = -1;

    setbuf(stdout, NULL);
    setbuf(stdin, NULL);

    ret_val = init_client(argc, (const char **) argv);

    if(-1 != ret_val)
    {
        run_client();

        ret_val = deinit_client();
    }

    printf("Client exited with value %d\n", ret_val);

    return ret_val;
}